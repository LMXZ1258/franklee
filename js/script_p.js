//   正在播放的音乐序号
let musicIndex = 1;
let slowm = 3;

window.addEventListener('load', () => {
  loadMusic(musicIndex);
  // 重新计算列表样式
  playingNow();
});

//  加载音乐
function loadMusic(indexNumb) {
  // 音乐名
  musicName.innerText = allMusic[indexNumb - 1].name;
  //   唱作人名
  musicArtist.innerText = allMusic[indexNumb - 1].artist;
  //   封面
  musicImg.src = `img/${allMusic[indexNumb - 1].name}.jpg`;
  //   音乐源
  mainAudio.src = `${allMusic[indexNumb - 1].src}`;
  playingNow();
}

// 播放音乐
function playMusic() {
  // 先给外层容器写上一个播放音乐的类标识
  wrapper.classList.add('paused');
  //   改变类进而改变图标
  playPauseBtn.querySelector('i').className = 'fas fa-pause';
  // 然后播放音乐
  mainAudio.play();
  // 重新计算列表样式
  playingNow();
}

// 暂停音乐
function pauseMusic() {
  // 移除播放标识
  wrapper.classList.remove('paused');
  //   改变类进而改变图标
  playPauseBtn.querySelector('i').className = 'fas fa-play';
  //   暂停播放
  mainAudio.pause();
  // 重新计算列表样式
  playingNow();
}

// 下一曲
function nextMusic() {
  //   改变当前值
  musicIndex++;
  if (repeatBtn.className.split(' ')[1] == 'fa-random')
  {
    musicIndex = Math.floor(Math.random()*(allMusic.length))//不知道是不是用+1
    if (musicIndex<=allMusic.length && musicIndex>=allMusic.length-slowm)
    {
      musicIndex = 24; 
    }
  }
  //   边界判定
  musicIndex > allMusic.length ? (musicIndex = 1) : (musicIndex = musicIndex);
  //   重新加载音乐
  loadMusic(musicIndex);
  //   播放
  playMusic();
  // 重新计算列表样式
  playingNow();
}

// 上一曲
function prevMusic() {
  //   改变当前值
  musicIndex--;
  if (repeatBtn.className.split(' ')[1] == 'fa-random')
  {
    musicIndex = Math.floor(Math.random()*(allMusic.length))//不知道是不是用+1
    if (musicIndex<=allMusic.length && musicIndex>=allMusic.length-slowm)
    {
      musicIndex = 24; 
    }
  }
  //   边界判定
  musicIndex < 1 ? (musicIndex = allMusic.length) : (musicIndex = musicIndex);
  //   重新加载音乐
  loadMusic(musicIndex);
  //   播放
  playMusic();
  // 重新计算列表样式
  playingNow();
}

// 为播放/暂停按钮绑定监听事件
